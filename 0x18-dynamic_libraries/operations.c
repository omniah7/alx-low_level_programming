#include "operations.h"
/**
 * add - adds two integers
 * @x: first number
 * @y: second number
 * Return: result
 */
int add(int x, int y)
{
	return (x + y);
}

/**
 * sub - subtracts two integers
 * @x: first number
 * @y: second number
 * Return: result
 */
int sub(int x, int y)
{
	return (x - y);
}

/**
 * mul - multipies two integers
 * @x: first number
 * @y: second number
 * Return: result
 */
int mul(int x, int y)
{
	return (x * y);
}

/**
 * div - divides two integers
 * @x: first number
 * @y: second number
 * Return: result
 */
int div(int x, int y)
{
	return (x / y);
}

/**
 * mod - returns the remainder
 * @x: first number
 * @y: second number
 * Return: result
 */
int mod(int x, int y)
{
	return (x % y);
}
