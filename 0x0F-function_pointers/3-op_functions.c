#include "3-calc.h"
/**
* op_add - adds two integers
* @a: the first int
* @b: the second int
* Return: the result
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_sub - returns the subtraction of two integers
* @a: the first int
* @b: the second int
* Return: the result
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul - returns the product of two integers
* @a: the first int
* @b: the second int
* Return: the result
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_div - returns the division of a by b
* @a: the first int
* @b: the second int
* Return: the result
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mod - returns the remainder of division of two integers
* @a: the first int
* @b: the second int
* Return: the result
*/
int op_mod(int a, int b)
{
	return (a % b);
}
